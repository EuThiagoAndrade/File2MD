    def __init__(self, default_lang: str = "pt"):
        self.current_lang = "pt"
        self.set_language(default_lang)

    def set_language(self, lang: str) -> None:
        """Define o idioma ativo."""
        if lang in self.TRANSLATIONS:
            self.current_lang = lang
        else:
            self.current_lang = "pt"

    def translate(self, key: str) -> str:
        """Retorna a tradução da chave no idioma ativo."""
        return self.TRANSLATIONS.get(self.current_lang, self.TRANSLATIONS["pt"]).get(key, key)
